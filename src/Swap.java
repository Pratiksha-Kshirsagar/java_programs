package pratiniha;

public class Swap {

	public static void main(String[] args) {
		System.out.println("before swap");
		int a =10, b =20, c;
		System.out.println(a);
		System.out.println(b);
		System.out.println("after swap");
		c=a;
		a=b;
		b=c;
		System.out.println(a);
		System.out.println(b);
		

	}

}
