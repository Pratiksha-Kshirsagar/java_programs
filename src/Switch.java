
public class Switch
{
    public static void main (String[]args)

{    
    int no=2;
    System.out.println("enter no");
    switch (no)
    {
        case 1:
        System.out.println("no is 1");
        break;

        case 2:
        System.out.println("no is 2");
        break;
        case 3:
        System.out.println("no is 3");
        break;

        default:
        System.out.println("invalid no");
        break;
    }

}
}